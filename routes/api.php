Route::group(['prefix' => 'auth'], function () {
    Route::post('register', [AuthController::class, 'register']);
    Route::post('login', [AuthController::class, 'login']);
    Route::put('update', [AuthController::class, 'update'])->middleware(['auth:api', 'verified']);
});
